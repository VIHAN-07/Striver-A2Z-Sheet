#include<iostream>
using namespace std;

int main() {
    // Data Types and Variables in C++
    
    // Integer types
    int a = 10;
    long long b = 1000000000LL;
    
    // Floating point types
    float c = 3.14f;
    double d = 3.14159;
    
    // Character and string
    char ch = 'A';
    string str = "Hello World";
    
    // Boolean
    bool flag = true;
    
    // Output all variables
    cout << "Integer: " << a << endl;
    cout << "Long long: " << b << endl;
    cout << "Float: " << c << endl;
    cout << "Double: " << d << endl;
    cout << "Character: " << ch << endl;
    cout << "String: " << str << endl;
    cout << "Boolean: " << flag << endl;
    
    return 0;
}
