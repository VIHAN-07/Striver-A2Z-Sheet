#include<iostream>
using namespace std;

int main() {
    // Basic Input/Output in C++
    
    // Taking input
    int a, b;
    cout << "Enter two numbers: ";
    cin >> a >> b;
    
    // Giving output
    cout << "Sum: " << a + b << endl;
    cout << "Difference: " << a - b << endl;
    cout << "Product: " << a * b << endl;
    
    return 0;
}
